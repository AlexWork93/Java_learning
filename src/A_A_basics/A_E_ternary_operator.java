package A_A_basics;

public class A_E_ternary_operator {
    public static void main(String[] args) {
        int num = 5;
        System.out.println(num == 5 ? "this is five" : "this is not five");
        num += 1;
        System.out.println(num == 5 ? "this is five" : "this is not five");

        //      CONDITION ? BLOCK IF CONDITION TRUE : BLOCK IF CONDITION FALSE
    }
}
